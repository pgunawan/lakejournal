            <p class="articletitle">About lake publishing society</p>
            <p>Lake Publishing Society (LPS) is a non-profit organization that supports educational, literary, and artistic endeavours by publishing visual art and writing engaged with the natural world, by supporting Canadian and international artists and writers, and by hosting readings, events, and benefits for our publications and for worthy environmental causes.<br><br>
In 2013, LPS ceased the publication of Lake Journal but it continues to publish fiction, poetry, critical essays, interviews, reviews and visual arts related to the environment in a variety of other formats and through a variety of projects.  We are committed to high production values, a contemporary design, and an editorial vision that is in search of exceptional art and thought. We are looking for invigorating, controversial and meditative works that are about our relation to the natural world.  We do not accept unsolicited manuscripts.<br><br>

<em>Lake</em>'s emblem, the hexagram called "The Joyous, Lake," is taken from an ancient Chinese oracular text, the I Ching, or Book of Changes. Part of the admonition of this hexagram is that "knowledge should be a refreshing and vitalizing force. It becomes so only with congenial friends with whom one holds discussion and practices application of the truths of life." The hexagram reminds us of ancient wisdom, global connection, and of the local since the singular geographical feature of the valley where the society is located is Okanagan Lake.<br><br>
LPS is run by a board of volunteers and a small but committed group of members who live, work and make art in the Okanagan Valley of British Columbia. 
