          <p class="articletitle">An Artist for Animals in Peril</p>
          <img src="images/illustrations/fall09_bill_bourne_image1.jpg" width="175" height="165" class="articleillustration" />
          <p class="author"><strong>a conversation with Bill Burns by Renay Egami</strong></p>
          <p>A children’s choir whose repertoire consists of the sounds of dogs, boats and airplanes, Italian mineral water for the Masked Puddle Frog, and a prosthetics program for animals with missing body parts: these are some of the ways in which artist Bill Burns considers the tensions between nature and culture. For the past twenty years, Burns has produced conceptually and socially engaged work that reminds us of what’s at stake both globally and closer to home.</p>
