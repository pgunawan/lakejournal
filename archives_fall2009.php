          <p class="articletitle">Artist Feature - tibora bea girczyc-blum</p>
          <p class="author"><img src="images/illustrations/fall09_tiborah_bea_image1.jpg" width="175" height="165" class="articleillustration" /></p>
          <p>My work dives deep into the cracks, the gaps between the rustic natural land, and the concrete confines of man. It is the interface between earth and man that is in constant flux. My art seeks to identify and document this interface, whether it is a plant springing up in the space between the curb or the trimming of trees to make way for electrical poles. The dichotomy of Nature and Man has informed and intrigued my lens.</p>
          <p class="readmorelink"><a href="archives_fall2009_tibora_bea.php">more..</a></p>
