          <p class="articletitle"><span class="author">Artist Feature</span> - <span class="author">joan heriot</span></p>
          <p class="author"><img src="images/illustrations/fall09_joan_heriot_image1.jpg" width="175" height="165" class="articleillustration" /></p>
          <p>Born in 1911, Joan Heriot has had two remarkable, long-spanning careers in her lifetime: one as a scientist, the other as an artist. Seen here are several<br />
            drawings of microscopic cross-sections of plants and of bones which Heriot produced for her scientific studies at university during the 1930s. These drawings<br />
            have been reproduced from her notebooks which now belong to Okanagan<br />
            College in Vernon, British Columbia.</p>
