          <p class="articletitle">Cool Moon Winter</p>
          <p class="author"><img src="images/illustrations/fall09_cool_moon_winter_2.jpg" width="175" height="165" class="articleillustration" />Lindsay Diehl</p>
          <p>We’ve talked for hours without really saying a thing. Have you noticed the winters keep getting longer? The sign by the lake’s edge warned not to walk across and pictured a stick man falling through the ice. In general, he had a hard time dealing with more than one thing at once, and so, pleaded for silence.</p>
