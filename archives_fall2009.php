          <p class="author"><img src="images/illustrations/fall09_notes_from_darker_ti.jpg" width="175" height="165" class="articleillustration" /></p>
          <p class="author"><strong>Lorin Schwartz</strong></p>
          <p>The interpretation of others is always an interesting place to begin thinking about a paper, and this one began at just such an occasion. Halfway through reading Whetstone1, a collection of poetry by British Columbian poet Lorna Crozier, I came across an article on the post-colonial poetics of Canadian literature. Whetstone was assigned reading for my final advanced doctoral methods course, a requirement in most faculties of education.</p>
