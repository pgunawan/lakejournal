           <p><span class="author">Don Gayton</span></p>
           <p><em>First Prize Winner of Lake’s Creative Non-Fiction Contest<br />
            Judged by Tim Bowling</em></p>
           <p>Trees do speak of local place, and none more so than the ponderosa pine. A tree native to southern British Columbia and native to northern Mexico, it defines dry landscapes from the Cascades and Sierras all the way eastward to the Hundredth Meridian. Favouring the dry, rocky and fire-prone places, ponderosa hosts exotic woodpeckers, provides thermal cover for wintering elk, and offers itself as maypole for dancing butterflies. It mingles with sagebrush and bunchgrass at low elevation, and consorts uneasily with Douglas-fir on the high. Rock faces and cliffs form frequent backdrops for the ponderosa pine. It grows in places heartrendingly beautiful to look upon, but tough to live in. I have known this tree since before I can remember; its sticky pitch endures on my clothes, my hands. The tree is resinous, and resonant.</p>
           <p>We share a common geography, this curious ponderosa and I. We are both Westerners, and we both carry all the paradoxical baggage that title implies.</p>
           <p>“Three-needled pine,” the book says, but often there are only two in a bundle, and occasionally five. Often as long as my hand, the bright green needles are grouped in radial green explosions around elegantly twisted, dark branches. The longest needles were favoured by First Nations women for weaving their elegant small baskets.<br />
           </p>
