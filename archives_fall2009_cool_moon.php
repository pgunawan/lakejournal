          <p class="articletitle">Cool Moon Winter</p>
           <div class="articleillustration">
             <p><img src="images/illustrations/fall09_cool_moon_winter_ima.jpg" width="345" height="229" /></p>
             </div>
           <p><span class="author">by Lindsay Diehl</span></p>
           <p>We’ve talked for hours without really saying a thing. Have you noticed the winters keep getting longer? The sign by the lake’s edge warned not to walk across and pictured a stick man falling through the ice. In general, he had a hard time dealing with more than one thing at once, and so, pleaded for silence. She, on the other hand, couldn’t stop talking and never told him what she was really thinking. These are temporary measures intended to keep the future alive. They were too tired to feel the cold. What I wanted to say was, “I love you.” The family vehicle was named “Sandy”. I’m looking for something to get me from here to there. The purpose was to slow things down or to speed things up. The trail has been covered by snow and marked by sunken footprints. The nights grow colder and my pillow gets weary. Is there anything more beautiful than a cool moon winter? “The truth is neither here nor there.” Outside, the trees have lost their leaves and their branches have gone brittle; down below, their roots have grown together and continue to dream. In other words, that was then, and this is now. The way is not clear. We’ve been blinded by cold. I thought the ducks had flown away, but they were at the far-end, huddled for heat. Somewhere beneath, a slow and steady beat.<br />
           </p>
