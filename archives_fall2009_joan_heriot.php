           <p><strong>Joan Heriot</strong><br />
               <strong>Journal Drawings</strong><br />
           </p>
           <p>Born in 1911, Joan Heriot has had two remarkable, long-spanning careers in her lifetime: one as a scientist, the other as an artist. Seen here are several<br />
             drawings of microscopic cross-sections of plants and of bones which Heriot produced for her scientific studies at university during the 1930s. These drawings<br />
            have been reproduced from her notebooks which now belong to Okanagan College in Vernon, British Columbia. This work is an early indication of Heriot’s precise rendering and scientific eye for detail, so evident in her later landscape pastels drawings.<br />
            <br />
             Joan Heriot developed a keen interest in entomology from her father, who aside from farming 18 acres of land, learned about science while writing in the Dominion Entomological Department located in Vernon’s first courthouse. Heriot says of her father: “He had an incredible gift for dissection and writing papers. He produced a number of scientific papers…and ended up making quite a name for himself.”</p>
           <p>In 1928, Joan Heriot began a degree course, first at Victoria College and then at the University of British Columbia, which would lead to research in entomology. Heriot hoped to obtain a position in insect pest research in relation to the fruit industry. However, she was told by a professor that no one in Canada would hire a female entomologist and that her only hope was to go to England. She worked for a year at odd jobs earning enough money to travel to Liverpool University where she continued her studies. She graduated in 1934 with honours in zoology with entomology. Funds being very low during the Depression, she had to obtain an education grant to complete her courses—which meant a Diploma in Education and a pledge to teach for four years after graduation. She never did become an entomologist but taught zoology instead (with entomology) for 30 years for the London University External Program at Brighton Technical College.<br />
           </p>
