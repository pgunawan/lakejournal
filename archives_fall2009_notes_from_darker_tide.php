          <p class="articletitle">Notes from a Darker Tide</p>
           <p><strong>Landscapes of the Personal in Lorna Crozier’s Whetstone </strong><strong>by Lorin Schwarz</strong></p>
           <p class="separator"><br />
           </p>
           <p>The interpretation of others is always an interesting place to begin thinking about a paper, and this one began at just such an occasion. Halfway through reading Whetstone1, a collection of poetry by British Columbian poet Lorna Crozier, I came across an article on the post-colonial poetics of Canadian literature. Whetstone was assigned reading for my final advanced doctoral methods course, a requirement in most faculties of education. Crozier’s poetry had literally taken my breath away; I was startled by her collapse of the external and internal worlds into a collection that effortlessly married the human with the ecological in a manner beyond even the most subtle metaphor. In Whetstone, we are presented with a world at once a projection of the psyche and a human reality existing integrally intertwined with nature. As Jane Hirshfield writes in Nine Gates: Entering the Mind of Poetry, “A landscape is never only outer; it is also a portrait of a state of soul.”2 In Whetstone, the portrait donates a view in both directions.</p>
