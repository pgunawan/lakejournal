           <p><strong>Dierdre Dwyer</strong></p>
           <p><strong><em>Mussel Shells</em></strong></p>
           <p>Fly past, gulls,<br />
             leave us those scatterings,<br />
             leftovers, your dishes:<br />
             salad bowl,<br />
             gravy boat,<br />
             ramekin,<br />
             spatula and spoon.<br />
             Aim well<br />
             for the rocks along shore road<br />
             are ragged. Break<br />
             your fast on mytilus edulis,<br />
             modiolus modiolus—<br />
             dear reader, watch your head<br />
             and pick up<br />
             these wings, drift<br />
             like wood through<br />
             pearly opalescence<br />
             that bleeds silver to blue<br />
             cyanosis to bruises<br />
             of aubergine, magenta, the colour<br />
             of grackles and damson plums—<br />
             imperial offerings<br />
             especially where the wings<br />
             are ribbed and hinged:<br />
             a locket.</p>
