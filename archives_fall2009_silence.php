          <p class="articletitle">The Silence of the Lodgepoles</p>
           <div class="articleillustration">
             <p><img src="images/illustrations/fall09_the_silence_image2.jpg" width="345" height="141" /></p>
             </div>
           <p><span class="author">by Barbara Klar</span></p>
           <p><em>Honourable Mention in Lake’s Creative Non-Fiction Contest<br />
            Judged by Tim Bowling</em></p>
           <p>I have walked uphill all morning. The meadows were triads of wildflowers: field chickweed, larkspur, buffalo bean, the meadows edged with white spruce and aspen, a few pines higher up. It was a long, steep climb, the land levelling to a sudden, miles-wide plateau of fescue and brome, distant dots of Black Angus, horizons of nothing but pine. Now, deep in the forest, in the quiet room of the shade, with the goodness of a sandwich and an apple in my belly, I can rest below the trees I have walked to every year since I was young. I am the only human for miles, and in the intervals between the gusts of southwest wind, the air is charged with a deep, moving silence. I can hear the blood in my ears. I can hear my tired heart. The wind rises again, the collective exhalation of all the gods who’ve walked here, the pines slightly rocking in the long breath out, one of them creaking now and again, floorboard in the attic of a great, lost house.</p>
           <p>These are the lodgepole pines of the Cypress Hills, a forest that is lost, misplaced from its home in the mountains west of here. I’m in southwest Saskatchewan, cattle country, and the hills rise strangely from the prairie like a place that shouldn’t be here, the highest land between the Rockies and Labrador, a great shoulder of sediment left by the waters over millions of slow years. And as the Pleistocene hunters were spearing the mastodons, the ice sheets were bulldozing south, parting around this nunatak of altitude, the air finally warming, the lodgepoles taking root in the montane hills, a small bright moon of the mountains.<br />
           </p>
