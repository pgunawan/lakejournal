          <p class="articletitle">Artist Feature - tibora bea girczyc-blum</p>
           <div class="horizontalillustrationcontainer" style="height:335px;">
             <p><img src="images/illustrations/fall09_tiborah_bea_image2.jpg" width="345" height="272" /></p>
             <div class="articleillustrationcaption"><strong>The Pinch 2008</strong> Archival Pigment Print 11” x 14”<br />
              Oakland, California</div>
           </div>
           <p>My work dives deep into the cracks, the gaps between the rustic natural land, and the concrete confines of man. It is the interface between earth and man that is in constant flux. My art seeks to identify and document this interface, whether it is a plant springing up in the space between the curb or the trimming of trees to make way for electrical<br />
            poles. The dichotomy of Nature and Man has informed and intrigued my lens.</p>
           <p>The Growth series began in 2004 when I was a student at the University of California, San Diego. In the art department, there was a plant that grew from the upper cement balcony, dangling its roots in search of the earth. One summer, the plant was ripped from its perch; four months later, it had grown back. While traveling, working, and living around the world, I have documented the resiliency of plants. Nature continues its quest for life in the most unnatural of places. These glimpses of green give me hope. If a plant can survive, so will we.<br />
           </p>
