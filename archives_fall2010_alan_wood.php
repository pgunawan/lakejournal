           <p><strong>In Conversation with Gary Pearson</strong></p>
           <p>Alan Wood's recent series, "The Bishop's Garden," furthers his longstanding bond with nature and the art of collage. Wood, widely known for his big-picture landscape subjects (rivers, forests, ravines, prairies, islands, ocean fronts), focuses in this series on the comparatively stable and scaled-down environment of the Bishop family's garden at their home in Vancouver. While it is tempting to refer to Alan Wood as a landscape artist, he has in his history of work with this subject created an artistic idiom distinctly his own.  Wood is primarily a studio artist, working from an idea or preliminary study, building compositions with collage elements and painting processes.  This feature on "The Bishop's Garden" consists of four major collage works as well as two of Wood's location studies done in the family's urban garden.</p>
<p>"Landscape" is a broad subject genre that contains sub-genres such as wilderness landscape, prairie landscape, urban landscape, garden landscape, and so on, with each category cultivating in a set of parameters based on conventions formed by their particular but frequently cross-referenced histories.  Likewise, the artistic genre of collage has its own frames of reference, adapting to new initiatives and technologies as it cycles through generational changes.  This is where a close consideration of Alan Wood's art is intriguing.
<br /></p>
