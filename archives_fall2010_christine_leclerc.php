          <p class="articletitle">You Deserve to Hate Everything</p>
           <p><strong>By Christine Leclerc</strong></p>
           <p class="separator"><br />
           </p>
           <p>Lissa eyes a calendar in a glass tower as a wind pushes a wall of cloud up the mountains on the north shore. Waves clip at the edge of the inlet and she pinches the skin between her eyelids. The pencil marks on the calendar blur. She manages to squint the court dates into focus, but the lines that keep the days apart go missing.</p>
<p>She sees a handful of people in rain-soaked work clothes.  They line up behind Thierry, Lissa’s boyfriend, who is always first in line at the bank in her daydreams.  He runs his hands through his gold hair so everyone can see how perfect it is.  Th teller says, “Next in line,” and Thierry’s eyes twinkle.  He notes the steps shaved into the sides of the teller’s head.  It’s the kind of detail that makes Lissa laugh over lunch at overly-expensive restaurants.</p>
<p>Thierry’s right shoulder rises and rolls back like a wave.  He cinches his wallet with the perfect fingers of his perfect right hand and slips it from the back pocket of his designer jeans like it’s nothing.</p>
<p>The teller pushes his glasses up the bridge of his nose in slow motion.  Ever since the <i>Urbane</i> interview, Thierry, and all who behold him, move in slow motion.  “How can I help you?” the teller asks.</p>
<p>Thierry slow-flicks his wallet open and slow-turns it upside down.  Change rings out on the counter while the cheques fan out in vellum-smooth silence.  Several seconds pass before Thierry opens his mouth to state that he’d like to make a deposit.
</p>
