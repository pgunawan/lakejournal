          <p class="articletitle">Zoom Lens</p>
           <p><strong>By Diana Hartog</strong></p>
           <p class="separator"><br />
           </p>
           <p>"The spare key...."<br>
I follow her out the front door. <br>
"...Is right here, on this ledge." <br>
I reach up, careful of my fresh manicure, and, feeling around, tap the key with a red fingernail. "Got it."
<p>”But I never lock.  Only at night.  Otherwise…” The owner shrugs.  She’s leaving for the Yucatan, ten days of sun.  “Please bring in the mail every day, and of course the newspaper from the driveway.”—Which will indicate to potential thieves that she <i>hasn’t</i> gone on vacation.
<p>The master bedroom rather dark, from a quick glimpse, suitcases gaping open and clothes scattered in indecisive piles on the queen-size bed.  “This will all be straightened before I go.”—That door quickly closed.
<p><i>House-plants?</i> Watered once a week.  The dishwasher self-explanatory.  Garbage bin placed out at the curb Tuesday nights. <i>What else?</i> Oh, the hot tub.  Two tablespoons of chlorine powder every five days.  Clean towels are in a hall closet.  She’s moving faster now through the house, looking for trouble.  “This faucet drips.”  What else…
</p>
