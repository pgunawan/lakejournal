          <p class="articletitle">Interview with Robert Arthur Alexie</p>
           <p><strong>By Lally Grauer</strong></p>
           <p class="separator"><br />
           </p>
           <p><b>Lally Grauer</b>: In a talk you gave recently, you said “If you don’t come from a society that has a connection to the land, you don’t know how strong it can be.”  Could you say more about that?</p>
<p><b>Robert Arthur Alexie</b>: It’s a hard thing to put into words because I have not been out on the land for many, many years, but I do know that when I go up the river from Fort McPherson to the land where my grandparents made a living many years ago, I feel connected to them.</p>
<p><b>LG</b>: What river do you mean?</p>
<p><b>RAA</b>: The Peel River.  The land is much like I describe in <i>Porcupines and China Dolls</i>: low rolling hills, birch, spruce, sand bars.  In the summer it’s green and smells like rebirth; in the fall there are the beautiful colours and in the winter it’s almost barren, but white and very beautiful.  It has many tributaries like the Snake, Bonnet Plume, and Wind Rivers.  These rivers are all protected under the Canadian Heritage River System—very few people go up there.  When I go up there I feel that connection, because that’s where I come from; that’s where my parents and grandparents made a living before they moved into the community of Fort McPherson.
</p>
