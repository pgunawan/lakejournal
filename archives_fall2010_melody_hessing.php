          <p class="articletitle">Sanctuary</p>
           <p><strong>By Melody Hessing</strong></p>
           <p class="separator"><br />
           </p>
           <p>Today an arctic front pierces Vancouver's winter gloom. Grabbing my binoculars, I jump in the car, heading south against lanes of lemming traffic, towards the Strait of Georgia. I tunnel under the Fraser River and into the lowlands of Delta, past the condos now sprouting Chung Chuck's old potato field. The car rattles across the one-lane creaky bridge to Westham Island.</p>
<p>Almost 80,000 lesser snow geese migrate south annually from their breeding grounds on Wrangel Island in Russia.  They over-winter in the Fraser and Skagit deltas on fields and rich estuarine foreshore.  Sometimes geese sprinkle the ground like popcorn—soil-smeared geese that grin with grassroots muck, dumpling-white geese with black tail feathers.  Today the bare fields are brown, except for the random sprawl of pumpkins, like a gym after basketball practice.</p>
<p>The parking lot at the Reifel Migratory Bird Sanctuary is almost empty this bright December day.  Perhaps people don’t know about the geese.  After all, it’s a weekday.  Maybe they’re at work, or school.  I pick my way through mallards gabbling for a handout of seed.  Along the path to the dike, sun-bronzed ponds float puddle ducks—baldpate widgeons, gadwalls and the green sheen of more mallards.  At the end of the trail, the dome of Mount Baker bulges silver-white in the pastel sun.
</p>
