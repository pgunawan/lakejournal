           <p><strong>Monique Verdin</strong></p>
           <p>At the base of the world's largest river delta, between Mississippi and the Atchafalaya, an endangered ridge known as Pointe aux Chenes stretches south out into the coastal wetlands of southeast Louisiana. Pointe aux Chenes is French for "point of the oaks"; but in lower Pointe aux Chenes, the oaks are dying.</p>
<p>We delta dwellers are living on the frontlines of global climate change while simultaneously bearing witness to infrastructure and industries’ unintended, ill effects. </p>
<p>At present, one acre of coastal south Louisiana erodes into the Gulf of Mexico every hour.  Navigation, exploration, and pipeline canals have allowed saltwater to penetrate inner marshes that once were brackish and fresh.  Levees built along the Mississippi River have controlled the wet-landscape’s demise, depriving the delta of natural floodwaters and crucial sediments necessary for wetlands to thrive. </p>
<p>Long before the BP drilling disaster erupted on April 20, 2010, south Louisiana was plagued by an endangered ecosystem, a fragile economy, and a way of life facing possible extinction.  The spill has only further exacerbated our plight.
<br /></p>
