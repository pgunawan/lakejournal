          <p class="articletitle">CUT BLOck</p>
           
            <p><strong>Hugh Anderson</strong></p>
            <p>We’re here to repopulate the woods. We tell our young selves this. Most of the thirty of us have seen pictures of clear-cut logging and we care. Still clean, still upright, keen to jump out of the crew cabs and plant some trees; we are wide-eyed idealists: it’s that straight-forward. But the pictures of clear cuts we knew had no sense of scale; so we’re a bit thrown when we walk into the first open ground of the season. It covers football fields and soccer fields and more, with a few basketball courts tucked around the edges, and we’re told this is a small one. The land rises up in slopes and swells, nowhere near flat; it undermines the cropped aerial photographs that only held two dimensions.</p>
            <p>Our new boots leave tracks and hold us to the path in the machine ruts we follow on foot. The sun rises with us, in silence, in the cold blue first light. “It’s so peaceful” kicks a doe out of bed. The deer’s grey winter hide is mottling to brown as it bounces amongst the grey-brown stumps and finally breaks the treeline. The treeline is that fence of trunks and (trimmed in) branches and roots (and shades) that bounds (the open space). A tuft of trees left at the crest of a ridge is a “wilderness patch” left for wildlife. I’d thought we were heading for the (view)top of the hill<br />
              but we divide the land into parcels<br />
              spread ourselves thin and comb over the surroundings<br />
            start on the ragged edge(of my piece, just inside the forest’s drip line,) and work in to the middle.</p>
            <p>I’ve already accepted that I’m the kind of person who, if lost and wandering in the bush(woods), wouldn’t(would) walk against(shout into) the wind of conventional wisdom and just stay put (until found) like you’re supposed to. But this is a clear-cut (and others have already passed this way). So at dinner of day four, while I’m trying to take in three thousand calories, I listen in on the advice at nearby tables: “you should always be sweating”, “work harder”, “work smarter”, “you don’t have to look at the tree you’re planting; look where you’re going to put the next one”, “singing makes it bearable”, “it works like a bear bell”, “stop to eat? It only takes one hand to bag up”, “don’t plant under water”, “quality counts”, “use your minimums&quot;. In the breakfast lineup the next morning, I pick up one more tidbit: “Don’t look back.”<br />
              <br />
             </p>
