           <p><strong>an excerpt by Don McKay</strong></p>
           
            <p>Assemblage: </p>
            <ul>
              <li>a gathering or bringing together</li>
              <li>a work of art consisting of miscellaneous objects brought into relation</li>
              <li>a biostratigraphic unit or level of strata characterized by a particular set of rocks, animals or plants</li>
            </ul>
            <p>The Muskwa Assemblage is a formation inside the Muskwa-Kechika wilderness which stretches from the Toad River area in the north to the Tuchodi Lakes in the south; some of its outcrops can be reached with a short hike from the Alaska Highway. It is one of those geological phenomena—such as unconformities, island arcs, and fossil beds—that open precipitously into deep time, like a storefront with a sign and window display which is in fact a façade for the abyss. “Muskwa Assemblage” names a set of rocks that share a common ancestry in Rodinia one and a half billion years ago. “Rodinia,” in turn, names the gigantic super-continent to which virtually all the earth’s land mass belonged. This makes the rocks of the assemblage older than almost all the other rocks in the Rockies, the same age as another Rodinian assemblage close to the American border far to the south. But if these rocks of the northern and southern Rockies belong to the same formation, what became of all the rocks that lay between them? Well, that intervening rock mass now forms most of (mind the abyss) Australia. When Rodinia broke up, like a finished jigsaw puzzle ponderously taking itself apart, life existed only in the ocean as single-celled prokaryotes. The atmosphere, so I read, had not yet accumulated much oxygen—a process which has to interest us, and which came about thanks to those very prokaryotes, whose photosynthesis created oxygen as a byproduct, just like plants today.</p>
           <p>Does it strike you how brave these words are—“photosynthesis,” “billion,” “prokaryote,” even “Australia,” even “outcrop”—to stand there and declare themselves for all the world as though they stood on solid platforms rather than trap doors?...</p>
