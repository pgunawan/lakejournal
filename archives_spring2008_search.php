          <p class="articletitle">Search: ken belford/invisible ink</p>
           
            <p><img src="images/illustrations/spring_08_search_ken_belf02.jpg" width="345" height="245" class="articleillustrationnocaption" /><strong>Barry McKinnon</strong></p>
            <p>Ken Belford’s career as a poet has
            followed an unusual path. Since the mid-1960’s, he lived exclusively and reclusively in the woods, mountains, and small northern towns of British Columbia.</p>
            <p>He has published only sporadically with small BC presses, and more recently with his own limited-run chapbook series
              off-set house and Vancouver’s Nomados Literary Publishers. “Woodsman,” “guide,” and “outdoorsman” have been the convenient
              but narrow definitions that preface most discussions of his poems.</p>
            <p>
             Between guiding and fishing seasons, Ken would go south a few times to live in Vancouver. And when Ken Belford (this “rumor and legend” as Al Purdy characterized him) was in Vancouver, the writers and poets would gather after his readings to listen to him talk within a complex range of poetry and poetics, ecology, politics, health and athletics, placism, and the north. For anyone who needed help with fly fishing or a chainsaw, he could break from the big discussion
             to demonstrate the proper technique for casting a floating line or filing chain rakers.<p>
             His first book, The Hungry Tide, self-published in Vancouver in 1965, isn’t on many bookshelves, including Ken’s, and the copies that do exist are most likely in California as part of the late bookseller Bill Hoffer’s archive—The
             Hungry Tide has “no matches” at AbeBooks.com. I remember seeing a copy of it in Hoffer’s notorious Canadian poetry collection in his shop in Gastown. It was a standard-size homemade-looking chapbook, a bluey-green cover with a title and hand drawing, stapled spine, and Gestetnered text. I don’t remember much about the poems now beyond the immediate and unmistakable Belford tone, and an intelligence and urgency of emotion that would continue to inform the writing ahead.<p>
             I first read Belford’s work in the early 1960’s in Calgary. Brad Robinson, a writer in touch with the West Coast literary scene, had urged me to submit my poems to Talon magazine. I became a regular contributor in the good company of David Phillips, bill bissett, Judith Copithorne, John Newlove, Pat Lane, Jim Brown, and Ken Belford. In a review of Belford’s second book, Fireweed (1967), Margaret Atwood wrote, “His poems read with the kind of inevitability of
             image and rhythm that makes other poets grit their teeth with envy.” I got to grit my teeth regularly for the next several years whenever a new issue of Talon slipped through the mail slot.
             </p>
             <p>As other little magazines—Gronk, blewointment, Repository, Tish, Iron, and No Money from the Government—did in those days, Talon became a community
             of writers who eventually met somewhere along the literary trail. I met Ken Belford in person one night in the fall of 1969. My first image of him was of a big man in the shadows getting out of a cab near our place on Queensway Street in Prince George. He had telephoned earlier to say he’d read my poems in Talon, that he was on his way to Vancouver to read in Al Purdy’s class at Simon Fraser University, and wondered if he could crash for the night. Hell yes! I said.Purdy’s invited me, too!</p>
             <p> I told Ken that I had read Fireweed with great pleasure and attention. It had become a much-discussed book among West Coast writers for its poetic weight and for its remarkable stanza-to-stanza leaps of thought. Many of his lines, for me, had become indelible—lines like this from his poem “Carrier Indians”:             </p>
             <blockquote>
               <p>People with large eyes.
                 <br />
                 Having nowhere to go:
                 <br />
                 I am one of them.             </p>
             </blockquote>
             <p>And these lines from “Omega”:             </p>
             <blockquote>
               <p>The first was to make my own law.
                 <br />
                 The second was to break it. 
                 To distinguish the limits.
                 <br />
                 Apparently the third is to pay for it.             </p>
             </blockquote>
             <p>That night we talked, drank beer and smoked late into the night and early dawn: two long lost and found poets in some kind of mysterious and large agreement—the beginning of a 38-year friendship. Later the next day I drove my wife, Joy, and Ken out to Highway 67 to hitch a ride to Vancouver. We were all broke, in our mid-twenties, and just starting out with real jobs—I was in my first shaky year as an English instructor at the College of New Caledonia. Most of our money went out to pay student loans, rents, and the high cost of living in the north. Plane travel was out of the question and the 1957 Plymouth my father had given me could barely make it to the high school free repair shop. So I left later that day after work on the overnight Greyhound and hooked up with everyone at SFU sometime around noon. Al Purdy met me outside a large steep lecture room, lit his cigar, and ushered me in. This was the first time I heard Ken Belford read. His was a voice connected to a serious and large rhythmic force...<br />
