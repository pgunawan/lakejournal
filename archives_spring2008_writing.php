           <p><strong>Writing Lake Superior</strong><br />
            Jenny Penberthy</p>
           <p>Lorine Niedecker’s “Lake Superior”—the first long poem she would see into print—occupies five pages with a total of 395 words. Her research and preparation for the poem, the punning and aptly named “millenium1 of notes for my magma opus” (Niedecker to Corman, August 20, 1966), numbers 260 mostly typed, single—spaced pages. Tens of thousands of words.</p>
           <p>In late July 1966 Lorine Niedecker and Al Millen set off in their Buick on a week-long journey around Lake Superior, “by way of L. Michigan shore to Mackinaw Country and Sault Ste. Marie…along the Ontario shore and down the Minn. side” (Niedecker to Corman, July 12, 1966). The impulse to research the “magma opus,” her epic of rocks and minerals (Davie 73), can be traced most directly to the previous summer’s road trip through the Black Hills of South Dakota. Niedecker remarked to Corman on the “[r]eddish gravel beside the paved roads and in a couple of places a pale gold driveway—covering with gold bits or yellow diamond sparkles all thru it!” and “[t]he big rock structures in the hills…merely greyish or pinkish or yellowish depending on the time of day” (July 28, 1965). A year later on 16 July 1966, just before leaving on the Lake Superior excursion, she wrote to Corman:</p>
           <blockquote>
             <p>You once spoke to me of rocks—someone there, is it Will Petersen?—<br />
              has an interest in them. I begin to see how one can have. I think our NW (Lake Superior region, Minn., Mich., Wis.) is not only for the geologist, a massive, grand corruption of nature. And of language (wonder if Bosho is still used in speech for Bon jour! Indian, French, British—The Northwest passage to the Orient has its Bosho only like a ton of rock. And weak verse like Longfellow’s Hiawatha. But some kind of poetry has been felt by several of the geologists in that region.</p>
             <p>I’m frantic when I remember that gold and diamond! driveway in South Dakota, not knowing what kind of stone or mineral it was. Probably a lot of quartz in it to give the shine.</p>
             <p>I’ll use a little time to walk beaches since this country is part of the agate, jasper, carnelian, Thompsonite region… Cid, no, I won’t be writing for awhile, and I need time, like an eon of limestone or gneiss…</p>
            </blockquote>
           <p>She mocks the naiveté of the previous year—“that gold and diamond! driveway”—as she readies herself for a journey in the field equipped now with facts and terminologies, matter for poetry.</p>
           <p>The Milwaukee Public Library provided abundant materials for her preparatory research. Books to consult on the road, however, had to be carefully selected. A handwritten scrap among the “Lake Superior” papers hints at dilemma: “Might take bird books on trip.” Note-taking was a necessary economy, but while habits of compression came easily to her, we notice that along the way her “pocketbook broke from weight of notebooks and stones.”</p>
           <p>Many of the “Lake Superior” notes serve this road trip; all of the notes serve the nascent poem. But the notes frustrate any search for direct evidence of the developing poem. One sees glimmers of the poem here and there—the “Chocolate River” section of the poem, for example, can be traced directly to the notes—but these glimmers are fleeting. However oblique the relationship between notes and poem may be, the “Lake Superior” notes offer rare access to<br />
            Niedecker’s poetics.</p>
           <p>The immediate impression they offer is of the vast body of facts lying behind a highly condensed poem. While only the “Lake Superior” notes have survived,3 there is some indication that extensive note-taking was a familiar feature of her practice. “I write from notes, grocery lists. I throw up my arms and scream: Write—cut it and just write poems” (Niedecker to Corman, Feb. 14, 1968). A comparable ratio of notes to final poem is suggested in a letter she wrote to Edward Dahlberg: “I wish I could do the birds, worms, plants of my little plot of earth here in the manner of the first explorers landing in Virginia and with my own human setting, mental furnishing etc… all the Greeks, your Bible people, everyone and all ideas strained, pointed to this. I might get 8 lines!” Writing “Lake Superior” appeared to require a mastery of facts. She sought out local geologists: “By the way, do you happen to know a geologist? I have stones from the Lake Superior region and here, and would like to identify” (to Ron Ellis, “Local Letters” 94). She told Corman she’d written “for geological maps from the office in Washington, D.C.” and, in the same letter, conveyed her scholarly zeal: “…almost all petrified wood is agate-ized wood, I’ve read. The circles in the agate are of growth? Dunno, aim to find out” (Aug. 20, 1966). She met Bob Nero at the Milwaukee Public Museum where, just before completing her first version of the poem, she continued “to research geology for her Lake Superior poem.”...<br />
