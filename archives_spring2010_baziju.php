          <p class="articletitle">Poems</p>
           <p><strong>Baziju</strong></p>
           <p class="separator"><br />
           </p>
           <p>Throughout the city are numerous patches of unintended wilderness. The stretch of abandoned railbed, evidently still in private hands, behind the loft conversion site on Castlefield. The steep slope along the western edge of a nearby park, traversed diagonally by a narrow footpath which, though overgrown in summer, makes a pleasant shortcut to the subway. The front yard of a tiny brick house down the street, for sale for more than six months now, the state of the yard mirroring, metaphorically, the state of the sales process—the sequence of “For Sale” signs marking a descent towards the ever-more-tolerant-but-less-familiar realtors—whether the result of tensions among the children who inherited it, or an unwillingness by one, or all, to part, finally, with what was once the family home.</p>
           <p>Taking that path to the subway in early autumn, it often strikes me that the steep, east-facing slope, with its various wildflowers and tall, ungainly, browning grasses, is the most beautiful of places in this otherwise quite ordinary suburban park. Many, it seems, agree, though last week a dissenting opinion presented itself in the form of a city worker who, having almost toppled from his ride-on mower, stood, arms akimbo, at the base of the slope, cursing under his breath and staring malevolently at the weedy provocation. If beauty is in the eye of the beholder, so too it seems is a certain vengeful theatricality which, though the work itself may be wearing, provides at least a serviceable (if in the end no more than temporary) antidote.</p>
