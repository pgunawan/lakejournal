          <p class="articletitle">Horses of the Ghost</p>
           <div class="horizontalillustrationcontainer">
             <p><img src="images/illustrations/spring10_christine_wiesenthal_img.jpg" /></p>
             <div class="articleillustrationcaption"><strong>Quad Damage in the Ghost Forest Marshland</strong><br />
              Maureen Enns Studio Ltd.</div>
