           <p><strong>Joe Rosenblatt</strong></p>
           <p><b>Elizabeth Bachinsky:</b> First off, let me say it’s a real pleasure to be able to speak to you directly about your work for LAKE. Thanks, Joe. If I understand your interests correctly, you welcome collaboration with other artists and often speak directly to their work. But, I wonder, is there ever a time when you feel the need to work entirely alone?</p>
           <p><b>Joe Rosenblatt:</b> Writing poetry is a solitary experience. For the most part, the poet, or for that matter, any creative writer, finds himself or herself alone in the process of writing original works that add to the sum of Canadian literature. Playwrights, I think, have it easier in that they have to workshop a play in progress. Such an experiential situation does not occur with poets. They are left alone with fragments of lines that they have to weave into a fabric that can be considered worthy of being a poem. I say yip! Yip! The solitary bard has to search his (or her) guts, as the poet Milton Acorn back in early sixties used to tell me when I complained that the muse had abandoned me. “Search your guts,” Milt used to rant at me. He was right. Ninety nine percent of the time I work alone in my writings and in my paintings and drawings. No one is there for me to guide me along the yellow brick road. I prefer it that way in general.</p>
           <p><b>EB:</b> Many of your poems that appear here in LAKE engage directly with the paintings of Canadian landscape artist Ken Kirkby. In the poem, “Silence is An Animal,” the speaker says, “Ken is familiar with the silence that stares back at him.” I can’t help but wonder how the poet-you is also familiar with the silence that stares back at him from the natural world. Your poems speak of “galactic muteness,” of “carborundum silence.” These are haunting images for me. What is this silence you speak of and where do you go in this busy urban world to find it? Does that silence frighten you?</p>
           <p><b>JR:</b> The darkness and stillness in Kirkby’s landscapes are self-evident. He paints solitary landscapes, people-less lakes and forest scenes. I intuit his vision, which I don’t find frightening, especially as I identify with that quietude, which seems quite natural to me. Kirkby is a literalist in his paintings. A fishing boat painting by Kirkby is exactly what you see. My vision of that realistic depiction of a fishing boat is that it is more than that: it is a boat that takes you into another world. His rocky beach paintings have that “galactic muteness” because they are inarticulate creatures. Pebbles don’t talk, clams and oysters do. Kirkby doesn’t do shellfish. <br /></p>
