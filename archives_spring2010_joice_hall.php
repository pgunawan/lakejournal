           <p><strong>Essay by Patricia Ainslie</strong></p>
           <p>At the core of Joice M. Hall’s art is realism, and a belief in the power of representation in its various forms within a nature-based imagery. From her early figurative work inspired by Byzantine art, to the male nude figures that culminated in her 1985-1987 Floating, to the images of religious festivals in Mexico and the landscapes of Alberta, British Columbia, and Mexico, Hall has remained true to her personal vision: a portrayal of the visible world, and a rigorous realism.</p>
	       <p>Landscape has been both an inspiration and a continuous thread in Hall’s work. After moving to the Okanagan in 1999, Hall embraced the landscape as her primary subject. She is inspired by the region’s idyllic and exceptional natural beauty. Her first landscape series painted here was the Okanagan Lake Panoramas of 2001, which were intended as postcard-perfect paintings to reflect the beauty of the area and to capture the light and the atmosphere. With meticulous observation of detail, she created almost topographical views, full of incident and particularity.</p>
           <p>In her next series, Storms and Rainbows (2003), though based in reality, Hall has increasingly selected, simplified and manipulated the detail, and the resulting painted images are bold and vivid. The paintings in the series are images of 180-degree views of particular weather occurrences, all based on photographs taken from the deck of her home, which overlooks Lake Okanagan. Depicting the atmospheric effects of the summer weather phenomena at various times of the day and in the evening, Hall has captured the transitory effects of nature, which, in reality, quickly change.<br /></p>
