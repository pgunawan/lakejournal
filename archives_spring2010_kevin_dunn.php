          <p class="articletitle">Perfect Chaos</p>
           <div class="horizontalillustrationcontainer">
             <p><img src="images/illustrations/spring10_kevin_dunn_img.jpg" /></p>
             <div class="articleillustrationcaption"><strong>Collecting Kiawe nectar</strong><br />
