          <p class="articletitle">False Economy</p>
           <p><strong>By Kim Goldberg</strong></p>
           <p class="separator"><br />
           </p>
           <p>It began, I now think, with the purpling leaves as the light fell. As my bare arms grew goosebumpy. As ravens and squirrels and tree frogs tucked into their evening roosts. As I scuffed my way through fir needle duff on my journey home. Or the illusion of home, since the true destination always remains a cipher until we arrive.</p>
           <p>Es dunkelt, I thought, although it had been thirty years since that German class in university. Es dunkelt rolled through my head like loose bolts in a tuna fish can. Loose bolts that must not be forgotten because they belong somewhere. Because they are waiting to re-attach something important.</p>
           <p>Leaves are not really purple. Except when es dunkelt. What are the true colours of the forest? If even hue is hostage to the vagaries of time-space, can anything be certain? And when did I lose my adaptation to sleep outdoors?</p>
           <p>But we have a bigger problem. We are approaching the point in the story that is the actual beginning, and written in the way I had hoped to tell it all. The rest was just a confused preamble tacked on as an afterthought. And one that somehow managed to launch itself in the Modern Realist Tradition. Thus setting you up, I fear, to expect that you will learn more about me as we progress. We want stories about people after all, not unvetted ideas. Leave symbolism to the Surrealists, abstraction to the metaphysicians. Just load us up with nuanced personal detail to engage vicarious emotion and choke out any hatchlings of unbounded thought. Perhaps you have already started to form a theory about the true reason for my detour through the woods, or for taking German in university. Perhaps you are even imagining a gullible student’s frothy affair with a German professor, a sexual act on a washing machine in a darkened laundry room at a faculty Christmas party at the Dean’s home. At the very least, you must by now have formed an opinion on my gender. But none of this is relevant. There are larger concerns at stake. </p>
