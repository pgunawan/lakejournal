          <p class="articletitle">Teachings of the Sacred Circle </p>
          <img src="images/illustrations/spring12_leah_dorion_thumb.gif" width="175" height="165" class="articleillustration" />
          <p class="author"><strong>Leah Dorion</strong></p>
          <p>For me, art is a spiritual expression. My work is intended to be multisensory. The paintings are tactile, and many times the paint is applied with my own fingers in order to help me connect on a deeper, physical level.  I often use other tools that help add dimension and depth to my art. My paintings are intended to be very visually stimulating. It is important to me to incorporate elements of indigenous beadwork to honour my First Nations and Metis women ancestors. In my paintings, I play with light, color value, texture, and movement.</p>
