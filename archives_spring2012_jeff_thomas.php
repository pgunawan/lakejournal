           <p><strong>Jeff Thomas</strong></p>
           <p>When I was a young boy, I visited my elder Emily General at the Six Nations Reserve and enjoyed listening to her tell stories about Iroquois culture and life on the reserve.  During one visit, Emily told me about the formation of the Iroquois Confederacy and its architect, a man called the Peacemaker. His vision of peace and unity would bring an end the deadly blood feuds among the Iroquois tribes living in present day New York State.</p>
<p>The Peacemaker’s work is commemorated on a wampum belt known as the Hiawatha Belt. The wampum belt has five icons representing the original five Iroquois nations that accepted his peace plan and from left to right they are the Seneca, the Cayuga, Onondaga, Oneida and Mohawk. The five icons are joined together by a white line which drifts into a negative or undefined space at  each end of the belt.
</p>

