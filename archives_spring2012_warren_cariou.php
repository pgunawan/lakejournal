          <p class="articletitle">An Athabasca Story</p>
           <p><strong>By Warren Cariou</strong></p>
           <p class="separator"><br />
           </p>
           <p>One winter day Elder Brother was walking in the forest, walking cold and hungry and alone as usual, looking for a place to warm himself. His stomach was like the shrunken dried crop of a partridge. It rattled around inside him as he walked, and with each step he took the sound made him shiver even more.</p>
           <p>Where will I find a place to warm myself? he wondered. Surely some relations will welcome me into their home, let me sit by the fire.</p>
           <p>But he walked for a very long time and saw none of his relations. Eventually he traveled so far west that he didn’t know the land anymore, and even the animals wouldn’t dare to help him because they knew how hungry he was .  They kept a safe distance. So he shivered and rattled his way further and further, without anything to guide him except the lengthening shadows and his unerring radar for trouble.</p>

