           <p><strong>Michael Nicoll Yahgulanaas</strong></p>
           <p>Once upon a time there was an Englishman who<br />
Said that he had named a Pacific Island after a<br />
British Queen. Some said that filled the emptiness<br />
Of the Haida inhabitants with the fullness of<br />
George’s wife Queen Charlotte.</p>

<p>Haida Manga challenges fundamental cartoon elements.<br />
Where a typical comic represents time and space as an<br />
Empty white gutter, Haida manga, assisted by Raven<br />
Reveals that time/space is an active, twisting and expanding<br />
Vitality where no island needs renaming.</p>

<p>This book is one of a continuing series<br />
Of episodes called Rocking Raven. These stories are<br />
All based on an old and foundational Haida narrative<br />
Called Raven Who Kept Walking.</p>

